-- 1. Crear todas las Jornadas (1 a 30) si no existen
DO $$ 
BEGIN
  FOR i IN 1..30 LOOP
    INSERT INTO public.jornadas (numero, division) 
    VALUES (i, 'segunda_andaluza')
    ON CONFLICT (numero, division) DO NOTHING;
  END LOOP;
END $$;

-- 2. Limpiamos partidos antiguos para evitar duplicados
DELETE FROM public.partidos WHERE TRUE;

-- 3. Función auxiliar para inserción rápida
CREATE OR REPLACE FUNCTION fast_match(j_num INT, loc TEXT, vis TEXT, f_fecha TIMESTAMPTZ) RETURNS VOID AS $$
BEGIN
  INSERT INTO public.partidos (jornada_id, equipo_local_id, equipo_visit_id, fecha_hora)
  SELECT 
    (SELECT id FROM jornadas WHERE numero = j_num AND division = 'segunda_andaluza' LIMIT 1),
    (SELECT id FROM equipos_reales WHERE nombre = loc LIMIT 1),
    (SELECT id FROM equipos_reales WHERE nombre = vis LIMIT 1),
    f_fecha;
END;
$$ LANGUAGE plpgsql;

